return {
	"stevearc/conform.nvim",
	event = { "BufReadPre", "BufNewFile" },
	config = function()
		local conform = require("conform")
		conform.setup({
		    formatters_by_ft = {
		    html = {"prettier"},
		    css = {"prettier"},
		    javascript = {"prettier"},
		    python = {"ruff"},
		    typescript = {"prettier"},
		    javascriptreact = {"prettier"},
		    typescriptreact = {"prettier"},
		    lua = {"stylua"},
		    c = {"clang-format"},
		    cpp = {"clang-format"}
		}
		})
		vim.keymap.set({ "n", "v" }, "<leader>mp", function()
			conform.format({
				lsp_fallback = true,
				async = false,
				timeout_ms = 500,
			})
		end, { desc = "Format file or range (in visual mode)" })
	end,
}
